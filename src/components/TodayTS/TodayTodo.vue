// ÈáçÊñ∞ÊéíÂ∫è
function relayoutPriority(todos: TodoWithNumberPriority[]) {
  // Ëé∑ÂèñÂ∑≤ÂÆåÊàê‰ªªÂä°ÁöÑ‰ºòÂÖàÁ∫ßÈõÜÂêà
  const lockedPriorities = new Set(
    todos
      .filter((t) => t.status === "done" && t.priority > 0)
      .map((t) => t.priority)
  );

  // Ëé∑ÂèñÊú™ÂÆåÊàê‰∏î‰ºòÂÖàÁ∫ß>0ÁöÑ‰ªªÂä°
  const active = todos
    .filter((t) => t.status !== "done" && t.priority > 0)
    .sort((a, b) => a.priority - b.priority);

  // Ëé∑ÂèñÊâÄÊúâÂèØÁî®ÁöÑ‰ºòÂÖàÁ∫ßÔºàÊéíÈô§Â∑≤ÈîÅÂÆöÁöÑÔºâ
  const availablePriorities = new Set<number>();
  for (let i = 1; i <= 10; i++) {
    if (!lockedPriorities.has(i)) {
      availablePriorities.add(i);
    }
  }

  // ÊåâÈ°∫Â∫èÂàÜÈÖçÂèØÁî®ÁöÑ‰ºòÂÖàÁ∫ß
  let priorityIndex = 0;
  active.forEach((t) => {
    const availablePriority = Array.from(availablePriorities)[priorityIndex];
    if (availablePriority) {
      t.priority = availablePriority;
      priorityIndex++;
    }
  });
}
// ÁªìÊùü‰ºòÂÖàÁ∫ßÁºñËæë
function finishEditing() {
  if (!editingTodo.value) return;

  // 1. ÁªüËÆ°Â∑≤ÂÆåÊàê‰ªªÂä°ÁöÑ‰ºòÂÖàÁ∫ßÈõÜÂêàÔºàË¶ÅÈîÅÂÆöÔºâ
  const lockedPriorities = new Set(
    props.todos
      .filter((t) => t.status === "done" && t.priority > 0)
      .map((t) => t.priority)
  );

  // 2. ÁªüËÆ°ÊâÄÊúâÊú™ÂÆåÊàê‰ªªÂä°
  const activeTodos = props.todos.filter(
    (t) => t.status !== "done" && t.priority > 0
  );

  // 3. ‰ºòÂÖàÁ∫ßË∞ÉÊï¥
  let desiredPriority = editingPriority.value;

  // Â¶ÇÊûúÁõÆÊ†á‰ºòÂÖàÁ∫ßÂ∑≤Ë¢´ÈîÅÂÆöÔºåÊòæÁ§∫ÊèêÁ§∫Âπ∂ÈÄÄÂá∫
  if (desiredPriority > 0 && lockedPriorities.has(desiredPriority)) {
    popoverMessage.value = "ËØ•‰ºòÂÖàÁ∫ßÂ∑≤Ë¢´Âç†Áî®";
    showPopover.value = true;
    setTimeout(() => {
      showPopover.value = false;
    }, 2000);
    editingTodo.value = null;
    return;
  }

  // 4. Ê£ÄÊü•ÊòØÂê¶ÁúüÁöÑÂèëÁîü‰∫ÜÂèòÂåñ
  if (editingTodo.value.priority === desiredPriority) {
    editingTodo.value = null;
    return;
  }

  // 5. ÂáÜÂ§áÊâπÈáèÊõ¥Êñ∞
  const updates: Array<{ id: number; priority: number }> = [];

  // Â¶ÇÊûúËÆæÁΩÆ‰∏∫0ÔºåÂçïÁã¨Â§ÑÁêÜ
  if (desiredPriority === 0) {
    updates.push({
      id: editingTodo.value.id,
      priority: 0,
    });
  } else {
    // Â§ÑÁêÜÂÜ≤Á™ÅÔºöÊâÄÊúâ >= Êñ∞‰ºòÂÖàÁ∫ßÁöÑÊú™ÂÆåÊàê‰ªªÂä°ÔºåÁºñÂè∑ÂæÄÂêéÊå™
    activeTodos.forEach((t) => {
      if (t.id !== editingTodo.value!.id && t.priority >= desiredPriority) {
        updates.push({ id: t.id, priority: t.priority + 1 });
      }
    });

    // ÂΩìÂâçÈ°πËµãÂÄº
    updates.push({
      id: editingTodo.value.id,
      priority: desiredPriority,
    });
  }

  // 6. Â∫îÁî®Êõ¥Êñ∞
  if (updates.length > 0) {
    emit("batch-update-priorities", updates);

    // Á´ãÂç≥Êõ¥Êñ∞Êú¨Âú∞Áä∂ÊÄÅ‰ª•Ëé∑ÂæóËâØÂ•ΩÁöÑÁî®Êà∑‰ΩìÈ™å
    updates.forEach((update) => {
      const todo = props.todos.find((t) => t.id === update.id);
      if (todo) todo.priority = update.priority;
    });

    popoverMessage.value = "‰ºòÂÖàÁ∫ßÂ∑≤Êõ¥Êñ∞";
    showPopover.value = true;
    setTimeout(() => {
      showPopover.value = false;
    }, 2000);
  }

  // ÈÄÄÂá∫ÁºñËæëÊ®°Âºè
  editingTodo.value = null;
  //  Á°Æ‰øù‰ºòÂÖàÁ∫ßËøûÁª≠
  relayoutPriority(props.todos);
}

